#include "libft.h"

int	ft_flagcheck(int a)
{
	if (a == '#' || a == ' ' || a == '+' || a == '-' || a == '0')
		return (1);
	return (0);
}

int	ft_speccheck(int a)
{
	if (a == 'c' || a == 's' || a == 'p' || a == 'd' || a == 'i' || \
	a == 'u' || a == 'x' || a == 'X' || a == '%')
		return (1);
	return (0);
}
